def century(year)
  result = ((year / 100) + 1)
  result -= 1 if year % 100 == 0
  result.to_s + century_suffix(result)
end

def century_suffix(century)
  return 'th' if (11..13).include?(century % 100)
  last_digit = century % 10

  case last_digit
  when 1 then 'st'
  when 2 then 'nd'
  when 3 then 'rd'
  else 'th'
  end
end

